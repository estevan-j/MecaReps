    private void btsGuardarRepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsGuardarRepActionPerformed
        ReparacionesModel reparacion;
        try {
            java.util.Date fechaUtil = fecha.getDate();
            java.sql.Date fechaSQL = new java.sql.Date(fechaUtil.getTime()); 
            Validaciones.validarDatosNumericos(txtPrecioRep.getText());
            reparacion = new ReparacionesModel(txtIDReparacion.getText(), cmbTaller.getSelectedItem().toString(), txtMatricula.getText(),fechaSQL ,txtTipoRep.getText(),descripcion.getText(), Double.parseDouble(txtPrecioRep.getText()));
            rep.guardarReparaciones(reparacion);
            JOptionPane.showMessageDialog(null, "Reparación Ingresada con éxito.", "Exitosa", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();  
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }      
    }//GEN-LAST:event_btsGuardarRepActionPerformed

