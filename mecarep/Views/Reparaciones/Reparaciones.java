    private void btnCancelarRep1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarRep1ActionPerformed
        try {
            // TODO add your handling code here:
            if (rep.verificarMatriculaExistente(txtMatricula.getText())) {
                txtIDReparacion.setEnabled(true);
                txtTipoRep.setEnabled(true);
                fecha.setEnabled(true);
                txtPrecioRep.setEnabled(true);
                cmbTaller.setEnabled(true);
                btsGuardarRep.setEnabled(true);
            }else {
                JOptionPane.showMessageDialog(null, "Matrícula No Registrada.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCancelarRep1ActionPerformed

