    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String cedula = this.txtCedula.getText();
        String apellido = this.txtApellidos.getText();
        String nombre = this.txtNombres.getText();
        try {
            Validaciones.validarNombre(nombre);
            Validaciones.validarApellido(apellido);
            String ciudad = this.cmbCiudad.getSelectedItem().toString();
            String idTaller = this.cmbTaller.getSelectedItem().toString();
            Cliente clienteguardar = new Cliente(nombre, apellido, idTaller, ciudad, cedula);  
            tabla.guardarCliente(clienteguardar);
            JOptionPane.showMessageDialog(null, "Cliente Ingresado con Ã©xito.", "Exitosa", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

